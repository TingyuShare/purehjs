#!/usr/bin/env node
// build-cheatsheets.js
// Downloads cheatsheets from https://github.com/cheat/cheatsheets
// and generates cheatsheets-data.js with embedded JSON data.
//
// Usage: node build-cheatsheets.js

import { writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";
const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);

const REPO = "https://raw.githubusercontent.com/cheat/cheatsheets/master/";

const TOPICS = [
  "tar", "git", "docker", "docker-compose", "curl", "wget", "grep", "find",
  "sed", "awk", "rsync", "scp", "ssh", "ssh-keygen",
  "npm", "python", "pip", "go",
  "systemctl", "journalctl", "crontab", "vim",
  "jq", "kubectl", "tmux", "screen",
  "ffmpeg", "zip", "unzip", "7z",
  "chmod", "chown", "ls", "cp", "mv", "rm",
  "mysql", "psql", "sqlite3",
];

function stripFrontMatter(text) {
  const lines = text.split("\n");
  if (lines[0].trim() === "---") {
    const end = lines.indexOf("---", 1);
    if (end !== -1) return lines.slice(end + 1).join("\n");
  }
  return text;
}

function parseCheatsheet(text) {
  const cleaned = stripFrontMatter(text);
  const lines = cleaned.split("\n");
  const entries = [];
  let desc = null;
  let codeLines = [];

  for (const line of lines) {
    if (line.startsWith("#")) {
      // Save previous entry
      if (codeLines.length > 0) {
        entries.push({ desc, code: codeLines.join("\n") });
        codeLines = [];
      }
      desc = line.replace(/^#\s*/, "").trim();
    } else if (line.trim()) {
      codeLines.push(line.trim());
    } else {
      // End of a block - save if we have code
      if (codeLines.length > 0) {
        entries.push({ desc, code: codeLines.join("\n") });
        codeLines = [];
        desc = null;
      }
    }
  }
  // Last block
  if (codeLines.length > 0) {
    entries.push({ desc, code: codeLines.join("\n") });
  }
  return entries;
}

async function fetchCheat(name) {
  try {
    const res = await fetch(REPO + name);
    if (!res.ok) return null;
    return await res.text();
  } catch {
    return null;
  }
}

const data = {};
const failed = [];

for (const topic of TOPICS) {
  const raw = await fetchCheat(topic);
  if (!raw || !raw.trim()) {
    failed.push(topic);
    continue;
  }
  const entries = parseCheatsheet(raw);
  if (entries.length === 0) {
    failed.push(topic);
    continue;
  }
  data[topic] = entries;
  console.error(`✓ ${topic} (${entries.length} entries)`);
}

if (failed.length > 0) {
  console.error(`\n✗ Failed: ${failed.join(", ")}`);
}

const banner = "// AUTO-GENERATED by build-cheatsheets.js from https://github.com/cheat/cheatsheets\n";
const output = banner + "var CHEATSHEETS = " + JSON.stringify(data, null, 2) + ";\n";

const outPath = join(__dirname, "cheatsheets-data.js");
writeFileSync(outPath, output, "utf-8");
console.error(`\n→ Generated ${outPath} (${Object.keys(data).length} topics)`);
